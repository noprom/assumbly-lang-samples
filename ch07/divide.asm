	LOCAL	COMP,OUT
	CNTR=0
	
	IFNDEF	DIVIDEND
	CNTR=1
	ENDIF
	
	IFNDEF	DIVISOR
	CNTR=1
	ENDIF
	
	IFNDEF	QUOTIENT
	CNTR=1
	ENDIF
	
	IF	CNTR
		EXITM
	ENDIF
	
